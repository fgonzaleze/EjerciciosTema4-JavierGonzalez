package ejercicio04;

public class Operaciones {

	double A; 					// Primer numero que escribe por teclado
	double B;					// Segundo numero que escribe por teclado

	public double suma() {
		double suma;
		suma = A + B;
		return suma;
	}

	public double resta() {
		double resta;
		resta = A - B;
		return resta;
	}

	public double multiplicacion() {
		double mult;
		mult = A * B;
		return mult;
	}

	public double division() {
		double div;
		div = A / B;
		return div;
	}

}
